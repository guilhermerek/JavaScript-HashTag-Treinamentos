//como funciona uma variavel no JS

const mensagem = "Olá, Mundo! Esta é uma constante.";
//const: constante, não altera o valor!

let mensagemNaVariavel = "Olá, Mundo! Esta é uma variavel.";
//let é o novo var, permite mudar o valor!!


console.log(mensagem);
console.log(mensagemNaVariavel);

mensagemNaVariavel = "Agora perceba! O quarto texto irá se alterar, pois fiz a mudança na frase!";

console.log(mensagem);
console.log(mensagemNaVariavel);